// Operator Aritmatika
// +, -, *, /, %
console.info(10 + 5);
console.info(10 - 5);
console.info(10 * 5);
console.info(10 / 5);
console.info(10 % 5);
console.info(10 % 3);
console.info(1 + 2 - (3 * 4) / 5);

// Operator Penugasan (Assigment)
let x = 10;
console.log(x);
x = 200;
console.log(x);
x += 5;
console.log(x);

// Operator Perbandingan
console.log(10 == 10);
console.log(10 == 15);
console.log(10 != 10);
console.log(10 != 15);
console.log(10 == `10`);
console.log(10 === `10`);
