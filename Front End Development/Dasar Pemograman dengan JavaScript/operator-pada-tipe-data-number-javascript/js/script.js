// Operator Aritmatika
// +, -, *, /, %
console.info(10 + 5);
console.info(10 - 5);
console.info(10 * 5);
console.info(10 / 5);
console.info(10 % 5);
console.info(10 % 3);
console.info(1 + 2 - (3 * 4) / 5);

// Operator Penugasan (Assigment)
let x = 10;
console.log(x);
x = 200;
console.log(x);
x += 5;
console.log(x);

// Operator Perbandingan
console.log(10 == 10);
console.log(10 == 15);
console.log(10 != 10);
console.log(10 != 15);
console.log(10 == `10`);
console.log(10 === `10`);

// Operator Logika
let y = 5;
console.info(y % 2 === 0);
y = 12;
console.info(y % 2 === 0 && y < 10);
console.info(y % 2 === 0 || y < 10);

// Operator Not
y = 10;
console.info(y < 10);
console.info(!(y < 10));
