let a = `Apple`;
let b = `Pen`;
console.info(a + b);
console.info(a + ` ` + b);

// Penjumlahan Number dengan String
console.info(10 + `10`);
console.info(`10` + 10);
console.info(10 + 10 + `10`);
console.info(`10` + 10 + 10);

// Operator Typeof
console.info(typeof 10);
console.info(typeof `10`);
console.info(typeof true);

// Operator Kondisional
let x = 10;
console.info(x % 2 === 0 ? `genap` : `ganjil`);
